// Scrivi un programma che dati 5 numeri restituisca in output la somma e la media.

let n1 = parseInt(prompt("Inserisci il primo Numero"));

let n2 = parseInt(prompt("Inserisci il secondo Numero"));

let n3 = parseInt(prompt("Inserisci il terzo Numero"));

let n4 = parseInt(prompt("Inserisci il quarto Numero"));

let n5 = parseInt(prompt("Inserisci il quinto Numero"));



let sum = n1 + n2 + n3 + n4 + n5;

let med = sum / 5;



console.log(`La somma dei numeri è: ${sum}, mentre la loro media: ${med}.`);

